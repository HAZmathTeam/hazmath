/** \mainpage Introduction
 *
 * The HAZMATH Finite Element (FE) and Solver library is built from C (C99 compatible) source files and
 * provides software components that can be used to simulate physical/social phenomena described by Partial
 * Differential Equations (PDEs), systems of PDEs, or graphs. We have included several standard discretizations
 * for PDEs, often used in various applications. Our examples range from simple scalar elliptic PDEs to systems
 * of PDEs, include time-dependent and nonlinear problems, and contain methods for the solution of linear
 * systems.
 *
 * Our aim is to provide a basic tool, which can be used to tackle specific problems on demand. We do not intend
 * to create a universal package that contains all discretizations available. HAZMATH contains the software to
 * solve numerical models based on finite-element/volume/difference discretizations, and our team can assist in
 * tweaking and adjusting these basic tools to meet the demands of the specific application. Thus, we aim to
 * complement the user's expertise and help build application-specific packages.
 *
 */
 
